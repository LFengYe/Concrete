package com.cn.bean;

/**
 *
 * @author LFeng
 */
public class CarDriverInfo {
    private static int recordCount;

    public static int getRecordCount() {
        return recordCount;
    }

    public static void setRecordCount(int aRecordCount) {
        recordCount = aRecordCount;
    }
    
    @FieldDescription(description = "车辆ID", operate = "import")
    private int carID;
    @FieldDescription(description = "车牌号", operate = "display")
    private String carNO;
    @FieldDescription(description = "司机ID", operate = "import")
    private int driverID;
    @FieldDescription(description = "司机名称", operate = "display")
    private String driverName;
    @FieldDescription(description = "备注", operate = "import")
    private String remark;

    public int getCarID() {
        return carID;
    }

    public void setCarID(int carID) {
        this.carID = carID;
    }

    public String getCarNO() {
        return carNO;
    }

    public void setCarNO(String carNO) {
        this.carNO = carNO;
    }

    public int getDriverID() {
        return driverID;
    }

    public void setDriverID(int driverID) {
        this.driverID = driverID;
    }

    public String getDriverName() {
        return driverName;
    }

    public void setDriverName(String driverName) {
        this.driverName = driverName;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }
    
}
